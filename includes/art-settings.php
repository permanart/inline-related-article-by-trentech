<p>Cara menggunakan plugin ini sangat <em>simple,</em> silahkan <em>copy shortcode</em> berikut pada artikel yang ingin dipasang :</p>
<h4><strong>[post jumlah=a tag="b"]</strong></h4>
a = Jumlah Artikel, kami buat maksimal 5 <br>
b = tag artikel yang diinginkan, apabila ingin lebih dari satu tag maka tambahkan plus <strong>+</strong>, contoh <strong>[post jumlah=3 tag="startup+bisnis"]</strong>. Apabila tag kamu lebih dari satu kata, misalkan kamu memiliki tag <em>"belajar koding"</em> maka diantara kedua kata tersebut tambahkan tanda <strong>-</strong>, contoh <strong>[post jumlah=3 tag="belajar-koding"].</strong> </br></br>

